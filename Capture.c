#include "IR.h"
#include "millis.h"
uint32_t u32Cmd, time;
/************************************************************************************************************/
/*    Main function                                                                                         */
/************************************************************************************************************/
void main (void)
{
	  P15_PUSHPULL_MODE;
    P15 = 0;
		
		millis_init();
		IR_Init();
	
    while (1) {
		if (IR_Check(&u32Cmd)) {
			switch (u32Cmd) {
			case BUTTON1:
				P15 = 0;
				break;
			case BUTTON2:
				P15 = 1;
				break;
			}
		}			
    }
}
